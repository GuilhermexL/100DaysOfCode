public class ExpressoesComparativas {

    public static void main(String[] args){

        int pao = 10;
        int acucar = 20;

        System.out.println(pao == acucar); // Igual
        System.out.println(pao != acucar); // Diferente
        System.out.println(pao > acucar); // Maior
        System.out.println(pao < acucar); // Menor
        System.out.println(pao >= acucar); // Maior ou igual
        System.out.println(pao <= acucar); // Menor ou igual
    }
}
